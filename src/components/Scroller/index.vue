<template>
    <div class="scroll" ref="scroll">
    <slot></slot>
    </div>
</template>

<script>
import BScorll from 'better-scroll'
export default {
    name:'Scroller',
    props:{
        handleToScroll:{
            type:Function,
            default:function(){},
        },
        handleToTouchEnd:{
            type:Function,
            default:function(){},
        }
    },
    mounted() {
          var scroll = new BScorll(this.$refs.scroll, {
            tap: true,
            probeType: 1
          });
          this.scroll=scroll;
          scroll.on('scroll',(pos)=>{
              this.handleToScroll(pos);
          });
          scroll.on('touchEnd',(pos)=>{
              this.handleToTouchEnd(pos);
          });

        
    },
    methods: {
        toScrollTop(y){
            this.scroll.scrollTo(0,y);
        }
    },
}
</script>
<style scoped>
    .scroll{height:100%;}

</style>